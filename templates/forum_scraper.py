def main():
    parser = argparse.ArgumentParser(description=f"Forum thread scraper for {SITE_NAME}")
    parser.add_argument("--sample", action="store_true", help="Scrape only 5 threads")
    parser.add_argument("--limit", type=int, default=None, help="Max threads to scrape")
    parser.add_argument("--input", type=str, default=None, help="Path to input URLs JSON file")
    parser.add_argument("--urls", nargs="+", default=None, help="Thread URLs as arguments")
    args = parser.parse_args()

    start_time = time.time()

    logger.info("=" * 80)
    logger.info(f"Starting forum thread scraper for {SITE_NAME}")
    logger.info(f"Site: {SITE_URL}")
    logger.info(f"Output: {OUTPUT_FILE}")
    logger.info("=" * 80)

    thread_urls = []

    if args.urls:
        thread_urls = args.urls
    elif args.input:
        thread_urls = load_urls_from_file(args.input)
    elif os.path.exists(INPUT_FILE):
        thread_urls = load_urls_from_file(INPUT_FILE)
    else:
        logger.error("No input_urls.json found and no URLs provided. Forum threads require URL list input.")
        sys.exit(1)

    if args.sample:
        thread_urls = thread_urls[:5]
    if args.limit:
        thread_urls = thread_urls[: args.limit]

    logger.info(f"Total threads to scrape: {len(thread_urls)}")

    results = []
    failed = 0

    for i, url in enumerate(thread_urls):
        result = fetch_page(url)
        if result:
            soup, status_code = result
            thread = extract_thread_from_page(soup, url, status_code, SRC_URL)
            thread["id"] = i + 1
            results.append(thread)
        else:
            logger.error(f"Failed to fetch: {url}")
            failed += 1

        if (i + 1) % 25 == 0:
            percent = ((i + 1) / len(thread_urls)) * 100
            logger.info(f"Progress: [{i + 1}/{len(thread_urls)}] ({percent:.1f}%)")

    output = {
        "site": {
            "name": SITE_NAME,
            "url": SITE_URL,
            "platform": PLATFORM,
            "scraping_method": SCRAPING_METHOD,
            "scraped_at": datetime.now(timezone.utc).isoformat(),
        },
        "threads": results,
        "metadata": {
            "scraping_duration_seconds": round(time.time() - start_time, 2),
            "failed_items": failed,
            "rate_limit_delay": DELAY_BETWEEN_REQUESTS,
        },
    }

    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        json.dump(output, f, indent=2, ensure_ascii=False)

    logger.info("=" * 80)
    logger.info("EXTRACTION COMPLETE")
    logger.info(f"Total: {len(results)}, Failed: {failed}")
    logger.info(f"Duration: {round(time.time() - start_time, 2)}s")
    logger.info(f"Output: {OUTPUT_FILE}")
    logger.info("=" * 80)


if __name__ == "__main__":
    main()
