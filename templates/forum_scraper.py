def extract_jsonld(soup: BeautifulSoup) -> Optional[dict]:
    forum_types = ("DiscussionForumPosting", "Question")
    for script in soup.find_all("script", type="application/ld+json"):
        try:
            data = json.loads(script.string)
            if isinstance(data, dict) and data.get("@type") in forum_types:
                return data
            if isinstance(data, list):
                for item in data:
                    if isinstance(item, dict) and item.get("@type") in forum_types:
                        return item
        except (json.JSONDecodeError, TypeError):
            continue
    return None


def extract_posts(soup: BeautifulSoup) -> list[dict]:
    posts = []
    post_els = soup.select("{POST_SELECTOR}")
    for i, post_el in enumerate(post_els):
        author_el = post_el.select_one("{POST_AUTHOR_SELECTOR}")
        content_el = post_el.select_one("{POST_CONTENT_SELECTOR}")
        date_el = post_el.select_one("{POST_DATE_SELECTOR}")

        post = {
            "post_number": i + 1,
            "author": author_el.get_text(strip=True) if author_el else "",
            "content": content_el.get_text(separator="\n", strip=True) if content_el else "",
            "timestamp": "",
        }
        if date_el:
            date_attr = date_el.get("datetime", "") or date_el.get("content", "") or date_el.get("title", "")
            post["timestamp"] = date_attr if date_attr else date_el.get_text(strip=True)

        posts.append(post)
    return posts


def extract_thread_from_page(soup: BeautifulSoup, url: str, status_code: int, src_url: str) -> dict:
    thread = {
        "id": 0,
        "title": "",
        "author": "",
        "posts": [],
        "views": 0,
        "replies": 0,
        "last_activity": "",
        "url": url,
        "src_url": src_url,
        "location": "",
        "status_code": status_code,
        "scraped_at": datetime.now(timezone.utc).isoformat(),
        "remarks": "",
    }

    jsonld = extract_jsonld(soup)

    # Title
    if jsonld:
        thread["title"] = jsonld.get("headline", jsonld.get("name", ""))
    else:
        el = soup.select_one("{TITLE_SELECTOR}")
        thread["title"] = el.get_text(strip=True) if el else (soup.title.string if soup.title else "")

    # Author (thread starter)
    if jsonld:
        author = jsonld.get("author", {})
        if isinstance(author, dict):
            thread["author"] = author.get("name", "")
        elif isinstance(author, str):
            thread["author"] = author
    else:
        author_el = soup.select_one("{AUTHOR_SELECTOR}")
        thread["author"] = author_el.get_text(strip=True) if author_el else ""

    # Posts
    thread["posts"] = extract_posts(soup)
    thread["replies"] = max(0, len(thread["posts"]) - 1)

    # Views
    views_el = soup.select_one("{VIEWS_SELECTOR}")
    if views_el:
        views_text = re.sub(r"[^\d]", "", views_el.get_text(strip=True))
        if views_text:
            thread["views"] = int(views_text)

    # Last activity
    last_el = soup.select_one("{LAST_ACTIVITY_SELECTOR}")
    if last_el:
        date_attr = last_el.get("datetime", "") or last_el.get("title", "")
        thread["last_activity"] = date_attr if date_attr else last_el.get_text(strip=True)

    return thread


