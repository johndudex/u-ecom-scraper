def clean_html(html_str: str) -> str:
    if not html_str:
        return ""
    text = re.sub(r"<[^>]+>", " ", html_str)
    text = unescape(text)
    return re.sub(r"\s+", " ", text).strip()


def make_product(product_data: dict, index: int, src_url: str, status_code: int = 200) -> dict:
    first_variant = product_data.get("variants", [{}])[0] if product_data.get("variants") else {}
    price = first_variant.get("price", "")
    compare_price = first_variant.get("compare_at_price", "")

    return {
        "id": index,
        "title": product_data.get("title", ""),
        "price": f"${price}" if price else "",
        "availability": "In Stock" if first_variant.get("available", False) else "Out of Stock",
        "original_price": f"${compare_price}" if compare_price and compare_price != price else "",
        "currency": "USD",
        "url": f"{SITE_URL}/products/{product_data.get('handle', '')}",
        "src_url": src_url,
        "location": "",
        "status_code": status_code,
        "scraped_at": datetime.now(timezone.utc).isoformat(),
        "remarks": "",
    }


# ═══════════════════════════════════════════════════════════════════════════════
# API FUNCTIONS
# ═══════════════════════════════════════════════════════════════════════════════

def fetch_with_retry(url: str, params: Optional[dict] = None) -> Optional[dict]:
    ssl_verify = proxy_config.config.get("strategy", {}).get("ssl_verify", False)
    escalation = proxy_config.get_escalation_tier()

    for tier in ["none", *escalation]:
        if should_warn_residential(tier):
            warn_residential_usage(url)

        proxies = proxy_config.get_proxy_dict(tier) if tier != "none" else None
        max_retries = proxy_config.get_max_retries(tier) if tier != "none" else MAX_RETRIES
        cooldown = proxy_config.get_cooldown(tier) if tier != "none" else DELAY_BETWEEN_REQUESTS * 2

        for attempt in range(max_retries):
            try:
                time.sleep(DELAY_BETWEEN_REQUESTS)
                response = requests.get(url, params=params, headers=HEADERS, proxies=proxies, timeout=proxy_config.get_timeout(), verify=ssl_verify)
                if response.status_code == 200:
                    return response.json()
                if proxy_config.is_banned(response.status_code, response.text):
                    logger.warning(f"Ban detected ({response.status_code}) on tier '{tier}', escalating...")
                    break
                response.raise_for_status()
            except requests.RequestException as e:
                logger.error(f"Request failed (attempt {attempt + 1}/{max_retries}, tier={tier}): {e}")
                if attempt < max_retries - 1:
                    time.sleep(cooldown)
    return None


def discover_product_urls_via_api(collection_url: str) -> tuple[list[str], list[dict]]:
    all_urls = []
    all_raw = []
    page = 1
    seen = set()

    while True:
        if "?" in collection_url:
            fetch_url = f"{collection_url}&limit={PRODUCTS_PER_PAGE}&page={page}"
        else:
            fetch_url = f"{collection_url}/products.json?limit={PRODUCTS_PER_PAGE}&page={page}"

        logger.info(f"Fetching page {page}: {fetch_url}")
        data = fetch_with_retry(fetch_url)

        if not data:
            logger.warning(f"No data returned for page {page}, stopping")
            break

        products = data.get("products", [])
        for product in products:
            handle = product.get("handle", "")
            product_url = f"{SITE_URL}/products/{handle}"
            if handle and product_url not in seen:
                seen.add(product_url)
                all_urls.append(product_url)
                all_raw.append(product)

        logger.info(f"Page {page}: {len(products)} products (total: {len(all_urls)})")

        if len(products) < PRODUCTS_PER_PAGE:
            break

        if not data.get("next_page"):
            break

        page += 1

    return all_urls, all_raw


def scrape_product(url: str, src_url: str) -> Optional[dict]:
    try:
        handle = url.rstrip("/").split("/")[-1]
        api_url = f"{SITE_URL}/products/{handle}.json"
        time.sleep(DELAY_BETWEEN_REQUESTS)
        response = requests.get(api_url, headers=HEADERS, timeout=15)
        response.raise_for_status()
        data = response.json()
        return make_product(data, 0, src_url, response.status_code)
    except Exception as e:
        logger.error(f"Failed to scrape {url}: {e}")
        return None


# ═══════════════════════════════════════════════════════════════════════════════
# INPUT HANDLING
