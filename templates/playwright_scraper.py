def clean_html(html_str: str) -> str:
    if not html_str:
        return ""
    text = re.sub(r"<[^>]+>", " ", html_str)
    text = unescape(text)
    return re.sub(r"\s+", " ", text).strip()


def get_browser(pw, headless=True, proxy=None):
    """Connect to browser — remote CDP if available, local launch otherwise."""
    cdp_endpoint = os.environ.get("BROWSER_CDP_ENDPOINT", "")
    if cdp_endpoint:
        logger.info(f"Connecting to remote browser via CDP: {cdp_endpoint}")
        return pw.chromium.connect_over_cdp(cdp_endpoint)

    launch_args = {"headless": headless, "args": ["--no-sandbox", "--disable-dev-shm-usage",
                "--disable-blink-features=AutomationControlled", "--disable-infobars",
                "--no-first-run"]}
    if proxy:
        launch_args["proxy"] = {"server": proxy}
    return pw.chromium.launch(**launch_args)


