EXTRACT_PRODUCT_JS = """
(src_url) => {
    const product = {
        title: '',
        price: '',
        availability: '',
        original_price: '',
        currency: '',
        url: window.location.href,
        src_url: src_url || '',
        location: '',
        remarks: ''
    };

    // JSON-LD
    const jsonldScripts = document.querySelectorAll('script[type="application/ld+json"]');
    let jsonld = null;
    for (const script of jsonldScripts) {
        try {
            const data = JSON.parse(script.textContent);
            if (data['@type'] === 'Product') { jsonld = data; break; }
            if (Array.isArray(data)) {
                for (const item of data) {
                    if (item['@type'] === 'Product') { jsonld = item; break; }
                }
            }
            if (jsonld) break;
        } catch(e) {}
    }

    // Title
    product.title = jsonld?.name ||
        document.querySelector('{TITLE_SELECTOR}')?.textContent?.trim() || '';

    // Price
    if (jsonld?.offers) {
        const offers = Array.isArray(jsonld.offers) ? jsonld.offers[0] : jsonld.offers;
        product.price = offers?.price || '';
        const highPrice = offers?.highPrice || '';
        if (highPrice && parseFloat(highPrice) > parseFloat(product.price || 0)) {
            product.original_price = highPrice;
        }
        product.currency = offers?.priceCurrency || 'USD';
        const avail = offers?.availability || '';
        product.availability = avail.includes('InStock') ? 'In Stock' : 'Out of Stock';
    } else {
        const priceEl = document.querySelector('{PRICE_SELECTOR}');
        product.price = priceEl?.textContent?.trim() || '';
        const origEl = document.querySelector('{ORIGINAL_PRICE_SELECTOR}');
        product.original_price = origEl?.textContent?.trim() || '';
        const stockEl = document.querySelector('{AVAILABILITY_SELECTOR}');
        if (stockEl) {
            const t = stockEl.textContent.toLowerCase();
            product.availability = t.includes('in stock') ? 'In Stock' : 'Out of Stock';
        }
    }

    return product;
}
"""

EXTRACT_PRODUCT_URLS_JS = """
() => {
    const links = document.querySelectorAll('{PRODUCT_LINK_SELECTOR}');
    const unique = new Set();
    links.forEach(a => {
        let href = a.getAttribute('href');
        if (href) {
            if (!href.startsWith('http')) {
                href = window.location.origin + href;
            }
            unique.add(href);
        }
    });
    return Array.from(unique);
}
"""


def scrape_product(page, url: str, src_url: str, index: int) -> dict:
    page.goto(url, wait_until="networkidle", timeout=PAGE_LOAD_TIMEOUT)
    page.wait_for_timeout(2000)

    data = page.evaluate(EXTRACT_PRODUCT_JS, src_url)

    return {
        "id": index,
        "title": data.get("title", ""),
        "price": data.get("price", ""),
        "availability": data.get("availability", ""),
        "original_price": data.get("original_price", ""),
        "currency": data.get("currency", ""),
        "url": url,
        "src_url": src_url,
        "location": data.get("location", ""),
        "status_code": 200,
        "scraped_at": datetime.now(timezone.utc).isoformat(),
        "remarks": data.get("remarks", ""),
    }


def discover_product_urls(page) -> list[str]:
    page.goto(PRODUCT_LISTING_URL, wait_until="networkidle", timeout=PAGE_LOAD_TIMEOUT)
    page.wait_for_timeout(3000)
    return page.evaluate(EXTRACT_PRODUCT_URLS_JS) or []


# ═══════════════════════════════════════════════════════════════════════════════
