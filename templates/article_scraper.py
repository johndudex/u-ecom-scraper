def extract_jsonld(soup: BeautifulSoup) -> Optional[dict]:
    article_types = ("Article", "NewsArticle", "BlogPosting", "TechArticle")
    for script in soup.find_all("script", type="application/ld+json"):
        try:
            data = json.loads(script.string)
            if isinstance(data, dict) and data.get("@type") in article_types:
                return data
            if isinstance(data, list):
                for item in data:
                    if isinstance(item, dict) and item.get("@type") in article_types:
                        return item
        except (json.JSONDecodeError, TypeError):
            continue
    return None


def extract_article_from_page(soup: BeautifulSoup, url: str, status_code: int, src_url: str) -> dict:
    article = {
        "id": 0,
        "title": "",
        "author": "",
        "publish_date": "",
        "content": "",
        "images": [],
        "tags": [],
        "category": "",
        "url": url,
        "src_url": src_url,
        "location": "",
        "status_code": status_code,
        "scraped_at": datetime.now(timezone.utc).isoformat(),
        "remarks": "",
    }

    jsonld = extract_jsonld(soup)

    # Title
    if jsonld:
        article["title"] = jsonld.get("headline", jsonld.get("name", ""))
    else:
        el = soup.select_one("{TITLE_SELECTOR}")
        article["title"] = el.get_text(strip=True) if el else (soup.title.string if soup.title else "")

    # Author
    if jsonld:
        author = jsonld.get("author", {})
        if isinstance(author, dict):
            article["author"] = author.get("name", "")
        elif isinstance(author, str):
            article["author"] = author
    else:
        author_el = soup.select_one("{AUTHOR_SELECTOR}")
        article["author"] = author_el.get_text(strip=True) if author_el else ""

    # Publish date
    if jsonld:
        article["publish_date"] = jsonld.get("datePublished", "")
    else:
        date_el = soup.select_one("{DATE_SELECTOR}")
        if date_el:
            date_attr = date_el.get("datetime", "") or date_el.get("content", "")
            article["publish_date"] = date_attr if date_attr else date_el.get_text(strip=True)

    # Content (main article body)
    if jsonld:
        article["content"] = jsonld.get("articleBody", "")
    else:
        content_el = soup.select_one("{CONTENT_SELECTOR}")
        if content_el:
            article["content"] = content_el.get_text(separator="\n", strip=True)

    # Images
    if jsonld:
        images = jsonld.get("image", [])
        if isinstance(images, str):
            images = [images]
        article["images"] = [img if isinstance(img, str) else img.get("url", "") for img in images if img]
    else:
        img_els = soup.select("{IMAGE_SELECTOR}")
        article["images"] = [make_absolute_url(img.get("src", img.get("data-src", ""))) for img in img_els if img.get("src") or img.get("data-src")]

    # Tags
    if jsonld:
        keywords = jsonld.get("keywords", "")
        if isinstance(keywords, str):
            article["tags"] = [k.strip() for k in keywords.split(",") if k.strip()]
        elif isinstance(keywords, list):
            article["tags"] = keywords
    else:
        tag_els = soup.select("{TAG_SELECTOR}")
        article["tags"] = [t.get_text(strip=True) for t in tag_els if t.get_text(strip=True)]

    # Category
    if jsonld:
        article["category"] = jsonld.get("articleSection", "")
    else:
        cat_el = soup.select_one("{CATEGORY_SELECTOR}")
        article["category"] = cat_el.get_text(strip=True) if cat_el else ""

    return article


# ═══════════════════════════════════════════════════════════════════════════════
# DISCOVERY
# ═══════════════════════════════════════════════════════════════════════════════

def discover_article_urls() -> list[str]:
    all_urls = []
    seen = set()
    page = 1

    while True:
        paginated_url = f"{ARTICLE_LISTING_URL}?page={page}"
        logger.info(f"Fetching listing page {page}: {paginated_url}")

        result = fetch_page(paginated_url)
        if not result:
            break

        soup, _ = result
        links = soup.select("{ARTICLE_LINK_SELECTOR}")
        for link in links:
            href = link.get("href", "")
            absolute_url = make_absolute_url(href)
            if absolute_url and absolute_url not in seen:
                seen.add(absolute_url)
                all_urls.append(absolute_url)

        logger.info(f"Page {page}: {len(links)} articles found (total: {len(all_urls)})")

        if len(links) == 0:
            break

        page += 1

    return all_urls


