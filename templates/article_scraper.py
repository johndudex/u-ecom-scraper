def main():
    parser = argparse.ArgumentParser(description=f"Article scraper for {SITE_NAME}")
    parser.add_argument("--sample", action="store_true", help="Scrape only 5 articles")
    parser.add_argument("--limit", type=int, default=None, help="Max articles to scrape")
    parser.add_argument("--input", type=str, default=None, help="Path to input URLs JSON file")
    parser.add_argument("--urls", nargs="+", default=None, help="Article URLs as arguments")
    args = parser.parse_args()

    start_time = time.time()

    logger.info("=" * 80)
    logger.info(f"Starting article scraper for {SITE_NAME}")
    logger.info(f"Site: {SITE_URL}")
    logger.info(f"Listing URL: {ARTICLE_LISTING_URL}")
    logger.info(f"Output: {OUTPUT_FILE}")
    logger.info("=" * 80)

    article_urls = []

    if args.urls:
        article_urls = args.urls
    elif args.input:
        article_urls = load_urls_from_file(args.input)
    elif os.path.exists(INPUT_FILE):
        article_urls = load_urls_from_file(INPUT_FILE)
    else:
        logger.info("No input_urls.json found. Discovering articles from listing page...")
        article_urls = discover_article_urls()
        save_urls_to_file(INPUT_FILE, article_urls)

    if args.sample:
        article_urls = article_urls[:5]
    if args.limit:
        article_urls = article_urls[: args.limit]

    logger.info(f"Total articles to scrape: {len(article_urls)}")

    results = []
    failed = 0

    for i, url in enumerate(article_urls):
        result = fetch_page(url)
        if result:
            soup, status_code = result
            article = extract_article_from_page(soup, url, status_code, SRC_URL)
            article["id"] = i + 1
            results.append(article)
        else:
            logger.error(f"Failed to fetch: {url}")
            failed += 1

        if (i + 1) % 25 == 0:
            percent = ((i + 1) / len(article_urls)) * 100
            logger.info(f"Progress: [{i + 1}/{len(article_urls)}] ({percent:.1f}%)")

    output = {
        "site": {
            "name": SITE_NAME,
            "url": SITE_URL,
            "platform": PLATFORM,
            "scraping_method": SCRAPING_METHOD,
            "scraped_at": datetime.now(timezone.utc).isoformat(),
        },
        "articles": results,
        "metadata": {
            "scraping_duration_seconds": round(time.time() - start_time, 2),
            "failed_items": failed,
            "rate_limit_delay": DELAY_BETWEEN_REQUESTS,
        },
    }

    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        json.dump(output, f, indent=2, ensure_ascii=False)

    logger.info("=" * 80)
    logger.info("EXTRACTION COMPLETE")
    logger.info(f"Total: {len(results)}, Failed: {failed}")
    logger.info(f"Duration: {round(time.time() - start_time, 2)}s")
    logger.info(f"Output: {OUTPUT_FILE}")
    logger.info("=" * 80)


if __name__ == "__main__":
    main()
